def is_deficient_number(n: int) -> bool:
    """
    Визначає, чи є задане натуральне число n недостатнім числом.
    Недостатнє число: сума власних дільників менша за саме число.

    Параметри:
    n (int): Натуральне число, яке потрібно перевірити.

    Повертає:
    bool: True, якщо число є недостатнім; False в іншому випадку.
    """
    if not isinstance(n, int) or n <= 0:
        raise ValueError("Вхідне значення має бути натуральним числом (ціле, > 0).")
        
    if n == 1:
        # Для 1 власних дільників немає (або лише 0). 
        # Сума власних дільників (дільники, крім самого n) для 1 - це 0. 0 < 1, тому 1 - недостатнє.
        return True

    sum_of_proper_divisors = 0
    # Перевіряємо дільники від 1 до sqrt(n)
    for i in range(1, int(n**0.5) + 1):
        if n % i == 0:
            # i є дільником
            
            # Власний дільник (крім самого n)
            if i < n:
                sum_of_proper_divisors += i
                
            # Знайдемо парний дільник (n / i)
            j = n // i
            
            # Якщо i і j різні та j є власним дільником (тобто j < n)
            if i != j and j < n:
                sum_of_proper_divisors += j
                
            # Оптимізація: якщо сума вже перевищує n, ми знаємо, що це не недостатнє число.
            if sum_of_proper_divisors >= n:
                return False
                
    # Якщо після перевірки всіх дільників сума менша за n, число є недостатнім.
    return sum_of_proper_divisors < n
